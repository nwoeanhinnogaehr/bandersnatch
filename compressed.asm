entry0:
; entry point
; this routine decompresses from data_section into output_addr ($8000)
; then jumps to $8000
; i gave up trying to understand it, and just ran it to grab the decompressed data
    LD HL,data_section
    LD DE,output_addr
    PUSH DE
    LD A,$80
    EX AF,AF' ; save AF
label1:
    LDI ; copy byte from data_section to output_addr
label2:
    LD BC,$02FF ; counter? BC is decremented by LDI
label3:
    EX AF,AF' ; grab original AF
label4:
    ADD A,A
    JR NZ,label5 ; if 2*A is nonzero, jump
    LD A,(HL) ; load size?
    INC HL ; move to next byte
    RLA ; rotate A left
label5:
    RL C ; rotate C left
    JR NC,label4 ; jump if leftmost bit of c was null
    EX AF,AF' ; save AF
    DJNZ label6 ; decrement b and jump if nonzero
    LD A,$02
    SRA C
    JR C,label9 ; jump if c&1
    INC A
    INC C
    JR Z,label8
    LD BC,$033F
    JR label3
label6:
    DJNZ label12
    SRL C
    JR C,label1
    INC B
    JR label3
label7:
    ADD A,C
label8:
    LD BC,$04FF
    JR label3
label9:
    INC C
    JR NZ,label15
    EX AF,AF'
    INC B
label10:
    RR C
    RET C ; conditional leave to output_addr (the only thing on the stack)
    RL B
    ADD A,A
    JR NZ,label11
    LD A,(HL)
    INC HL
    RLA
label11:
    JR NC,label10
    EX AF,AF'
    ADD A,B
    LD B,$06
    JR label3
label12:
    DJNZ label13
    LD A,$01
    JR label16
label13:
    DJNZ label14
    INC C
    JR NZ,label15
    LD BC,$051F
    JR label3
label14:
    DJNZ label7
    LD B,C
label15:
    LD C,(HL)
    INC HL
label16:
    DEC B
    PUSH HL
    LD L,C
    LD H,B
    ADD HL,DE ;hl += de
    LD C,A ;number of bytes to copy
    LD B,$00
    LDIR ;copy from hl to de and increment. repeat while bc-- != 0
    POP HL
    JR label2
; end of code section

