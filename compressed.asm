; Original QR code program
; The program data is compressed. A small loader decompresses it before
; running it. I don't recommend looking to closely at this file. I didn't
; take the time to reverse the decompression logic.
; decompressed.asm contains the decompressed & relocated code and should
; be much more readable.
;
; assembles into a .tap file

org 0

;begin header block
dw 0x0013 ; length of data

db 0x00 ; header marker byte
db 0x00 ; program type
db 0x42,0xc6,0x60,0x8c,0x72,0x73,0x4e,0xac,0x7f,0x48 ; filename
dw end_lbl-begin_lbl-1 ; length of data block
dw 0x0001 ; autostart line number
dw 0x06dc ; variable area
db 0xbd ; checksum
;end of header

;begin data block
